fun regexDivisibleBy(n: Int): String {
    when(n) {
        2-> "^(0|11*0)*$"
        3-> "^(0|1(01*0)*1)*$"
        4-> "^(0|1(1|01)*00)*$"
        5-> "^(0|1((10)*(0|11)(01*01)*01*00)*(10)*(0|11)(01*01)*1)*$"
        6-> "^(0|(11|1(00|011*0)(00|011*0)*1)(11|1(00|011*0)(00|011*0)*1)*0)*$"
        else -> "^(0|1)*$"
    }

    return "^(0|1(10)*(0|11)(01*01|01*00(10)*(0|11))*1)*$" // do it!
}